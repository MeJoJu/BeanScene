using Microsoft.EntityFrameworkCore;
using BeanScene2.Data;
using BeanScene2.Models;
using Microsoft.AspNetCore.Authorization;
using BeanScene2.Services;

namespace BeanScene2.Controllers
{
    [Authorize(Roles = "admin")]

    public class UsersController : Controller
    {
        private readonly BeanScene2Context _context;
        //private readonly IFileService _iFService;

        public UsersController(BeanScene2Context context/*, IFileService iFService*/)
        {
            _context = context;
            //_iFService = iFService;
        }
        // GET: Users
        public async Task<IActionResult> Index()
        {
            var beanScene2Context = _context.User.Include(u => u.Permission);
            return View(await beanScene2Context.ToListAsync());
        }
        
        // GET: Users/Details/5
        public async Task<IActionResult> Details(int? id)
        {
            if (id == null || _context.User == null)
            {
                return NotFound();
            }

            var user = await _context.User
                .Include(u => u.Permission)
                .FirstOrDefaultAsync(m => m.UserId == id);
            if (user == null)
            {
                return NotFound();
            }

            return View(user);
        }

        // GET: Users/Create
        public IActionResult Create()
        {
            ViewData["PermissionId"] = new SelectList(_context.Permission, "PermissionId", "PermissionId");
            return View();
        }

        // POST: Users/Create
        // To protect from overposting attacks, enable the specific properties you want to bind to.
        // For more details, see http://go.microsoft.com/fwlink/?LinkId=317598.
